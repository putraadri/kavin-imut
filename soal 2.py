# Tuple awal berisi angka campuran
data = (9, 2, 5, 1, 7, 3)

# Ubah tuple menjadi list agar bisa diurutkan
list_data = list(data)

# Urutkan list dari angka terkecil ke terbesar
list_data.sort()

# Tampilkan hasil tuple yang sudah diurutkan
print("Tuple hasil:", tuple(list_data))

# Tampilkan elemen pada index ke-2
print("Elemen index ke-2:", list_data[2])